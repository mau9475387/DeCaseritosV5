    private void color(){
        if(actual == 1){
            comBtn.setBackground(Color.white);
            comL.setForeground(new Color(247,87,26));
            
            restBtn.setBackground(new Color(247,87,26));
            restL.setForeground(Color.white);
        }
        
        if(actual==2){
            restBtn.setBackground(Color.white);
            restL.setForeground(new Color(247,87,26));
            
            comBtn.setBackground(new Color(247,87,26));
            comL.setForeground(Color.white);
        }
    }
